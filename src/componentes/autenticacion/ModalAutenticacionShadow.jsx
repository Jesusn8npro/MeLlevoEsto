import React, { useEffect, useRef, useState } from 'react'
import { createPortal } from 'react-dom'
import { X, Eye, EyeOff, Mail, User } from 'lucide-react'
import { useNavigate } from 'react-router-dom'
import { useAuth } from '../../contextos/ContextoAutenticacion'
import { clienteSupabase } from '../../configuracion/supabase'

export default function ModalAutenticacionShadow({ abierto, onCerrar }) {
  const navigate = useNavigate()
  const { iniciarSesion, registrarse, obtenerRutaRedireccion, usuario } = useAuth()
  const shadowRef = useRef(null)
  const hostElRef = useRef(null)
  const [mountNode, setMountNode] = useState(null)

  const [vista, setVista] = useState('login')
  const [cargando, setCargando] = useState(false)
  const [error, setError] = useState('')
  const [mensaje, setMensaje] = useState('')
  const inputRef = useRef(null)

  const [email, setEmail] = useState('')
  const [contrasena, setContrasena] = useState('')
  const [mostrarPass, setMostrarPass] = useState(false)

  const [nombre, setNombre] = useState('')
  const [apellido, setApellido] = useState('')
  const [emailReg, setEmailReg] = useState('')
  const [passReg, setPassReg] = useState('')
  const [mostrarPassReg, setMostrarPassReg] = useState(false)

  const [emailRec, setEmailRec] = useState('')

  useEffect(() => {
    if (!abierto) return
    const host = document.createElement('div')
    host.setAttribute('id', 'auth-shadow-host')
    document.body.appendChild(host)
    hostElRef.current = host
    const sr = host.attachShadow({ mode: 'open' })
    shadowRef.current = sr
    const mount = document.createElement('div')
    sr.appendChild(mount)
    const style = document.createElement('style')
    style.textContent = `
      :host{all:initial}
      .ov{position:fixed;inset:0;background:rgba(0,0,0,.7);backdrop-filter:blur(6px);display:flex;align-items:center;justify-content:center;z-index:100003;padding:20px}
      .bx{width:100%;max-width:460px;max-height:92vh;background:#fff;border-radius:22px;box-shadow:0 24px 60px rgba(0,0,0,.28);overflow:auto;position:relative;font-family:Inter,system-ui,-apple-system,Segoe UI,Roboto,Helvetica,Arial,sans-serif}
      .cl{position:absolute;top:16px;right:16px;width:36px;height:36px;border-radius:50%;border:1px solid #e2e8f0;background:#f8fafc;color:#64748b;display:flex;align-items:center;justify-content:center;cursor:pointer}
      .br{padding:36px 24px 20px;background:linear-gradient(135deg,#ff6b35 0%,#ff8c42 100%);display:flex;align-items:center;justify-content:center}
      .ct{padding:26px 24px 28px}
      .tt{font-size:22px;font-weight:800;color:#1e293b;text-align:center;margin:0 0 8px}
      .ds{color:#64748b;text-align:center;font-size:14px;margin:0 0 18px}
      .fm{display:flex;flex-direction:column;gap:16px}
      .rw{display:grid;grid-template-columns:1fr 1fr;gap:12px}
      .fd{display:flex;flex-direction:column;gap:6px}
      .fd label{font-size:13px;font-weight:600;color:#475569}
      .in{position:relative;display:flex;align-items:center}
      .in input{width:100%;padding:12px 42px 12px 14px;border:1.5px solid #dbe2ea;border-radius:12px;background:#f8fafc;font-size:15px;color:#1e293b}
      .in input:focus{outline:none;border-color:#ff6b35;background:#fff;box-shadow:0 0 0 4px rgba(255,107,53,.12)}
      .ic{position:absolute;right:12px;color:#94a3b8}
      .tg{position:absolute;right:12px;background:none;border:none;color:#94a3b8;cursor:pointer;padding:4px;border-radius:6px}
      .tg:hover{color:#ff6b35;background:rgba(255,107,53,.08)}
      .bn{width:100%;padding:14px;border:none;border-radius:12px;background:linear-gradient(135deg,#ff6b35 0%,#ff8c42 100%);color:#fff;font-size:15px;font-weight:700;box-shadow:0 8px 20px rgba(255,107,53,.28);cursor:pointer}
      .bn:disabled{opacity:.6;cursor:not-allowed}
      .sp{display:flex;align-items:center;justify-content:center;color:#94a3b8;font-size:13px;margin:14px 0}
      .gg{width:100%;display:flex;align-items:center;justify-content:center;gap:10px;border:1.5px solid #dbe2ea;border-radius:12px;padding:12px;background:#fff;cursor:pointer}
      .ln{display:flex;flex-direction:column;gap:8px;text-align:center;margin-top:10px}
      .lk{background:none;border:none;color:#64748b;cursor:pointer;font-size:14px;padding:6px;border-radius:6px}
      .lk:hover{color:#ff6b35}
      .mg{text-align:center;font-size:14px;border-radius:10px;padding:10px}
      .mg.err{background:#fef2f2;color:#dc2626;border:1px solid #fecaca}
      .mg.ok{background:#f0fdf4;color:#16a34a;border:1px solid #bbf7d0}
      @media(max-width:640px){.bx{max-width:420px;border-radius:20px}.rw{grid-template-columns:1fr}}
      @media(max-width:480px){.bx{max-width:360px}}
    `
    sr.appendChild(style)
    setMountNode(mount)
    return () => {
      try {
        if (hostElRef.current) {
          document.body.removeChild(hostElRef.current)
        }
      } catch {}
      shadowRef.current = null
      hostElRef.current = null
      setMountNode(null)
    }
  }, [abierto])

  useEffect(() => { if (abierto && inputRef.current) setTimeout(() => inputRef.current?.focus(), 100) }, [abierto, vista])
  useEffect(() => { if (abierto && usuario) onCerrar() }, [abierto, usuario])
  useEffect(() => { document.body.style.overflow = abierto ? 'hidden' : ''; return () => { document.body.style.overflow = '' } }, [abierto])

  const limpiar = () => { setError(''); setMensaje(''); setEmail(''); setContrasena(''); setNombre(''); setApellido(''); setEmailReg(''); setPassReg(''); setEmailRec(''); setMostrarPass(false); setMostrarPassReg(false) }

  const manejarLogin = async (e) => {
    e.preventDefault(); setError(''); setCargando(true)
    if (!email || !contrasena) { setError('Completa todos los campos'); setCargando(false); return }
    try {
      const r = await iniciarSesion(email, contrasena)
      if (r.error) { setError(r.error); setCargando(false) } else { onCerrar(); setTimeout(() => navigate(obtenerRutaRedireccion()), 300) }
    } catch { setError('Error de conexión'); setCargando(false) }
  }

  const manejarRegistro = async (e) => {
    e.preventDefault(); setError(''); setCargando(true)
    if (!nombre || !apellido || !emailReg || !passReg) { setError('Completa todos los campos'); setCargando(false); return }
    if (passReg.length < 6) { setError('La contraseña debe tener al menos 6 caracteres'); setCargando(false); return }
    try {
      const r = await registrarse(emailReg, passReg, { nombre: `${nombre} ${apellido}` })
      if (!r.success) { setError(r.error); setCargando(false) } else { onCerrar(); setTimeout(() => navigate(obtenerRutaRedireccion()), 300) }
    } catch { setError('Error de conexión'); setCargando(false) }
  }

  const manejarRecuperar = async (e) => {
    e.preventDefault(); setMensaje(''); setCargando(true)
    if (!emailRec) { setMensaje('Ingresa tu correo'); setCargando(false); return }
    try {
      const { error } = await clienteSupabase.auth.resetPasswordForEmail(emailRec, { redirectTo: `${window.location.origin}/restablecer-contrasena` })
      setMensaje(error ? error.message : 'Revisa tu correo para restablecer tu contraseña')
    } catch { setMensaje('Error de conexión') } finally { setCargando(false) }
  }

  const loginGoogle = async () => {
    setCargando(true)
    try {
      const { error } = await clienteSupabase.auth.signInWithOAuth({ provider: 'google', options: { redirectTo: window.location.origin } })
      if (error) setError(error.message)
    } catch { setError('Error de conexión con Google') } finally { setCargando(false) }
  }

  const cerrarPorOverlay = (e) => { if (e.target === e.currentTarget) onCerrar() }
  if (!abierto || !mountNode) return null

  return createPortal(
    <div className="ov" onClick={cerrarPorOverlay} role="dialog" aria-modal="true">
      <div className="bx" onClick={(e) => e.stopPropagation()}>
        <button className="cl" onClick={onCerrar} aria-label="Cerrar"><X size={18} /></button>
        <div className="br">
          <img src="/MeLlevoEsto.Com Logo.png" alt="ME LLEVO ESTO" onError={(e) => { e.currentTarget.src = '/images/Logo oficial me llevo esto.jpg' }} />
        </div>
        {vista === 'login' && (
          <div className="ct">
            <h2 className="tt">¡Bienvenido de nuevo!</h2>
            <p className="ds">Accede a tu cuenta y disfruta de los beneficios.</p>
            <form className="fm" onSubmit={manejarLogin}>
              <div className="fd">
                <label htmlFor="email">Correo electrónico</label>
                <div className="in">
                  <input id="email" ref={inputRef} type="email" value={email} onChange={(e) => setEmail(e.target.value)} placeholder="ejemplo@correo.com" required />
                  <Mail className="ic" size={18} />
                </div>
              </div>
              <div className="fd">
                <label htmlFor="pass">Contraseña</label>
                <div className="in">
                  <input id="pass" type={mostrarPass ? 'text' : 'password'} value={contrasena} onChange={(e) => setContrasena(e.target.value)} placeholder="Tu contraseña" required />
                  <button type="button" className="tg" onClick={() => setMostrarPass(!mostrarPass)}>{mostrarPass ? <EyeOff size={18} /> : <Eye size={18} />}</button>
                </div>
              </div>
              {error && <div className={`mg err`}>{error}</div>}
              <button type="submit" className="bn" disabled={cargando}>{cargando ? 'Ingresando...' : 'Entrar'}</button>
            </form>
            <div className="sp"><span>o continúa con</span></div>
            <button className="gg" onClick={loginGoogle} disabled={cargando}>
              <svg viewBox="0 0 24 24" width="20" height="20"><path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4"/><path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/><path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/><path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/></svg>
              <span>{cargando ? 'Conectando...' : 'Continuar con Google'}</span>
            </button>
            <div className="ln">
              <button className="lk" onClick={() => { setVista('recuperar'); limpiar() }}>¿Olvidaste tu contraseña?</button>
              <button className="lk" onClick={() => { setVista('registro'); limpiar() }}>¿No tienes cuenta? <b>Regístrate</b></button>
            </div>
          </div>
        )}
        {vista === 'registro' && (
          <div className="ct">
            <h2 className="tt">Crear Cuenta Nueva</h2>
            <p className="ds">Únete y accede a beneficios exclusivos.</p>
            <form className="fm" onSubmit={manejarRegistro}>
              <div className="rw">
                <div className="fd">
                  <label htmlFor="nombre">Nombre</label>
                  <div className="in">
                    <input id="nombre" ref={inputRef} type="text" value={nombre} onChange={(e) => setNombre(e.target.value)} placeholder="Juan" required />
                    <User className="ic" size={18} />
                  </div>
                </div>
                <div className="fd">
                  <label htmlFor="apellido">Apellido</label>
                  <div className="in">
                    <input id="apellido" type="text" value={apellido} onChange={(e) => setApellido(e.target.value)} placeholder="Pérez" required />
                    <User className="ic" size={18} />
                  </div>
                </div>
              </div>
              <div className="fd">
                <label htmlFor="emailReg">Correo electrónico</label>
                <div className="in">
                  <input id="emailReg" type="email" value={emailReg} onChange={(e) => setEmailReg(e.target.value)} placeholder="ejemplo@correo.com" required />
                  <Mail className="ic" size={18} />
                </div>
              </div>
              <div className="fd">
                <label htmlFor="passReg">Contraseña</label>
                <div className="in">
                  <input id="passReg" type={mostrarPassReg ? 'text' : 'password'} value={passReg} onChange={(e) => setPassReg(e.target.value)} placeholder="Mínimo 6 caracteres" required />
                  <button type="button" className="tg" onClick={() => setMostrarPassReg(!mostrarPassReg)}>{mostrarPassReg ? <EyeOff size={18} /> : <Eye size={18} />}</button>
                </div>
              </div>
              {error && <div className={`mg err`}>{error}</div>}
              <button type="submit" className="bn" disabled={cargando}>{cargando ? 'Registrando...' : 'Crear Cuenta'}</button>
            </form>
            <div className="ln">
              <button className="lk" onClick={() => { setVista('login'); limpiar() }}>¿Ya tienes cuenta? <b>Inicia Sesión</b></button>
            </div>
          </div>
        )}
        {vista === 'recuperar' && (
          <div className="ct">
            <h2 className="tt">Recuperar Contraseña</h2>
            <p className="ds">Ingresa tu correo y te enviaremos un enlace.</p>
            <form className="fm" onSubmit={manejarRecuperar}>
              <div className="fd">
                <label htmlFor="emailRec">Correo electrónico</label>
                <div className="in">
                  <input id="emailRec" ref={inputRef} type="email" value={emailRec} onChange={(e) => setEmailRec(e.target.value)} placeholder="ejemplo@correo.com" required />
                  <Mail className="ic" size={18} />
                </div>
              </div>
              {mensaje && <div className={`mg ${mensaje.includes('Revisa') ? 'ok' : 'err'}`}>{mensaje}</div>}
              <button type="submit" className="bn" disabled={cargando}>{cargando ? 'Enviando...' : 'Enviar Enlace'}</button>
            </form>
            <div className="ln">
              <button className="lk" onClick={() => { setVista('login'); limpiar() }}>Volver a Iniciar Sesión</button>
            </div>
          </div>
        )}
      </div>
    </div>,
    mountNode
  )
}
